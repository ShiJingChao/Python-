"""
    字符串的变形
"""
# upper
print('pYthOn'.upper())  # PYTHON

# lower
print('123PYthON'.lower())  # 123python

# swapcase
print('Love Python'.swapcase())  # lOVE pYTHON

# title
print('i love python'.title())  # I Love Python
print('i*love*python'.title())  # I*Love*Python

# capitalize
print('i love python'.capitalize())  # I love python
print('**i love python'.capitalize())  # **i love python

# \n 换行 \t 制表符
# 将字符串中的制表符\t转换为空格, 共8个字符
print('i\tlove python'.expandtabs())
print('my\tname is lixue'.expandtabs())



